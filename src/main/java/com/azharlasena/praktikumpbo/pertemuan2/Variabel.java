package com.azharlasena.praktikumpbo.pertemuan2;

public class Variabel {
    
    public static void main(String[] args) {
        
        //Pendeklarasian Variabel nama dengan tipe data String yang memiliki nilai "Azhar Lasena"
        String nama = "Azhar Lasena";
        //Pendeklarasian Variabel usia dengan tipe data Int yang memiliki nilai "19"
        int usia = 19;
        
        //Pemanggilan Variabel 'nama' teramsuk nilainya
        System.out.println("Nama : " + nama);
        //Pemanggilan Variabel 'usia' teramsuk nilainya
        System.out.println("Usia : " + usia);
        
    }
    
}
